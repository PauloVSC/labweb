package com.ufba.bean;

public class Aluno {

	private int rm;
	private String name;
	
	public Aluno() {
		super();
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRm() {
		return rm;
	}
	public void setRm(int rm) {
		this.rm = rm;
	}
	
	
}
